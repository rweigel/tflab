[h,t] = z2h([1,1,1]);
assert(all(h == [1,0,0]));
assert(all(t == [0,1,-1]));

[h,t] = z2h([1,1,1]');
assert(all(h == [1,0,0]'));
assert(all(t == [0,1,-1]'));
