%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Example 1
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

N  = 100;
dN = 3;
w  = 2;
[~,f] = fftfreq(N);
[fe,Ic,Ne] = evalfreq(N,[3,2],'linear');
ts = sprintf('evalfreq(%d,[%d,%d],''linear'')',N,dN,w);

fprintf('###############################################\n');
fprintf('%s\n',ts);
fprintf('###############################################\n');

evalfreq_plot(N,[3,2],'linear');
set(get(gca,'Title'),'String',ts);

fprintf('###############################################\n');

